package model;

public class JenisMember {
    private String id;
    private String nama;

    // Getter dan Setter untuk id
    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    // Getter dan Setter untuk nama
    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return nama;
    }
}
